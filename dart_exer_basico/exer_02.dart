//Declare duas variáveis numéricas e realize as operações básicas de adição, subtração, multiplicação e divisão entre elas.

void main() {
  num n1 = 4;
  num n2 = 8.8;

  print('a soma de $n1 e $n2 é: ${n1 + n2}');
  print('a subtração de $n1 e $n2 é: ${(n1 - n2).toStringAsFixed(2)}');
  print('a multiplicação de $n1 e $n2 é: ${n1 * n2}');
  print('a divisão de $n1 e $n2 é: ${(n1 / n2).toStringAsFixed(2)}');
}
